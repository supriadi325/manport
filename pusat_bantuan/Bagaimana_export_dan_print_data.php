<p>Untuk print ataupun export data, kamu tinggal meng-klik tombol dengan icon pdf <span class="fa fa-file-pdf-o"></span> atau tombol dengan icon print <span class="fa fa-print"></span>, dan kamu akan diarahkan ke halaman <i>pdf viewer</i>. Lalu di pojok kanan atas terdapat icon print(untuk print data) dan icon download(untuk mendownload file dalam bentuk pdf).</p>
<img src="<?= config::base_url('assets/img/bantuan/print_export_1.png'); ?>">
<p>Dikarenakan untuk setiap browser print datanya sedikit berbeda, disini akan dicontohkan dalam dua browser yaitu Mozilla Firefox dan Google Chrome</p>

<p style="margin-top: 20px; text-indent: 0;">Print data di <b>Mozilla Firefox</b></p>
<ol style="margin-top: 10px;">
	<li>Klik tombol print dipojok kanan atas pdf viewer</li>
	<li>Lalu akan menampilkan window baru seperti gambar dibawah ini</li>
	<img class="width50" src="<?= config::base_url('assets/img/bantuan/print_export_2.png'); ?>">
	<li>Untuk ukuran kertas sudah diatur <b>A4</b> dan Layout Orientation <b>Potrait</b>, jadi kamu tidak perlu lagi untuk mengatur apapun, sebelum print pastikan dibagian Printer &raquo; Name sesuai dengan nama printer kamu</li>
	<li>Pada bagian Print range pastikan memilih All untuk print semua halaman atau Pages untuk print halaman yang kita tentukan sendiri</li>
	<li>Jika sudah maka klik tombol ok</li>
</ol>

<p style="margin-top: 20px; text-indent: 0;">Print data di <b>Google Chrome</b></p>
<ol style="margin-top: 10px">
	<li>Klik tombol print dipojok kanan atas pdf viewer, jika tidak ada maka arahkan cursor ke pojok kanan atas</li>
	<li>Lalu akan menampilkan window baru seperti gambar dibawah ini</li>
	<img src="<?= config::base_url('assets/img/bantuan/print_export_3.png'); ?>">
	<li>Untuk ukuran kertas sudah diatur <b>A4</b> dan Layout Orientation <b>Potrait</b>, jadi kamu tidak perlu lagi untuk mengatur apapun, sebelum print pastikan dibagian Destination sesuai dengan nama printer kamu</li>
	<li>Pada bagian Pages pastikan all untuk print semua halaman atau custom untuk print halaman yang kita tentukan sendiri</li>
	<li>Jika sudah maka klik tombol Print</li>
</ol>