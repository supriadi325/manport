<ol>
	<li>Jika Export atau Print surat keterangan lulus telah dilakukan, dan tidak ada yang salah dari hasil Export atau Print-nya, serta Export atau Print Transkip nilai juga telah dilakukan, dan tidak ada yang salah dari hasil Export atau Print-nya, maka sebaiknya hapus data Siswanya!</li>
