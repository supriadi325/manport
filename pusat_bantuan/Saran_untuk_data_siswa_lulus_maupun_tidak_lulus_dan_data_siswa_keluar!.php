</ol>
<p>Untuk data Siswa Keluar:</p>
<ol>
	<li>Jika Export atau Print surat keterangan keluar dan masuk telah dilakukan, dan tidak ada yang salah dari hasil Export atau Print-nya, maka sebaiknya hapus data Siswanya!</li>
	<li>Harap diperhatikan, data Siswa yang telah dihapus tidak akan bisa dikembalikan lagi, maka pastikan untuk menyimpan data Export surat keterangan keluar dan masuk-nya</li>
</ol>